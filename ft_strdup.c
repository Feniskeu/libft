#include "libft.h"

char	*ft_strdup(char *src)
{
	int		i;
	char	*cpy;

	if (!src)
		return (NULL);
	i = 0;
	cpy = (char *)malloc(sizeof(char) * ft_strlen(src)+ 1);
	if (!cpy)
		return (NULL);
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
